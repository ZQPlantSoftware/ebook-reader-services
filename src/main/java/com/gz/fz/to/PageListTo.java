package com.gz.fz.to;

public class PageListTo {

	
	private Integer id;
	private String cfi;
	private Integer page;
	private Double height;
	private String chapter_id;
	private String cfi_start;
	private String cfi_end;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCfi() {
		return cfi;
	}
	public void setCfi(String cfi) {
		this.cfi = cfi;
	}
	public Integer getPage() {
		return page;
	}
	public void setPage(Integer page) {
		this.page = page;
	}
	public Double getHeight() {
		return height;
	}
	public void setHeight(Double height) {
		this.height = height;
	}
	public String getChapter_id() {
		return chapter_id;
	}
	public void setChapter_id(String chapter_id) {
		this.chapter_id = chapter_id;
	}
	public String getCfi_start() {
		return cfi_start;
	}
	public void setCfi_start(String cfi_start) {
		this.cfi_start = cfi_start;
	}
	public String getCfi_end() {
		return cfi_end;
	}
	public void setCfi_end(String cfi_end) {
		this.cfi_end = cfi_end;
	}
	
}
